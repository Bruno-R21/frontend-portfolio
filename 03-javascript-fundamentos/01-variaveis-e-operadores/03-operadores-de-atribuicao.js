/* 
Crie uma variável numero iniciando em 7.
Primeiro, incremento ela em 3 usando +=.
Depois, use o operador ++ para incrementar 1.
Em seguida, diminua 2 usando -=.
*/

let numero =7

numero += 3
console.log("incremento: ", numero)

numero++
console.log("incremento: ", numero)

numero -= 2
console.log("Decremento: ", numero)





