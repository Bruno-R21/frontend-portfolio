
function somar(a,b){
    return(a+b);
}
function subtrair(a,b){
    return(a-b);
}
function multiplicar(a,b){
    return(a*b);
}
function dividir(a,b){
    return(a/b);
}

console.log("Soma:", somar(10,4));
console.log("Subtração:", subtrair(10,4));
console.log("Multiplicação:", multiplicar(10,4));
console.log("Divisão:", dividir(10,4));
